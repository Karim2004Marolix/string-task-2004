#3.Write a python program to check given character is vowel or consonant.
letter = input("please enter letter: ")
if int(letter == 'A' or letter == 'a' or letter == 'E' or letter == 'e' or letter == 'I' or letter == 'i' or letter == 'O' or letter == 'o' or letter == 'U' or letter == 'u'):
    print(letter,"vowel")
else:
    print(letter,"consonant")